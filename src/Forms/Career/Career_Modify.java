    private void doClose(int retStatus) {
        returnStatus = retStatus;
        setVisible(false);
        dispose();
    }
    public void receiveData(Career modifier) {
        careerModifier = modifier;
        if (modifier != null) {
           NameTextField.setText(modifier.getName());
            TitleComboBox.setSelectedItem(modifier.getTitleIntermediate());
            DescriptionTextField.setText(modifier.getDescription());
            DurationTextField.setText(String.valueOf(modifier.getDurationMonths()));
            TypeComboBox.setSelectedItem(modifier.getIdTypeCareer());
            ModalityComboBox.setSelectedItem(modifier.getIdModality());
            BranchComboBox.setSelectedItem(modifier.getIdBranch());
            RangeComboBox.setSelectedItem(modifier.getIdRange());
        }
    }

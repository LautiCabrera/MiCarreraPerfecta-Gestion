    public static void selectedRow(JTable table){
        int selectedRow = table.getSelectedRow();
        if (selectedRow != -1) {
            // Toma datos especificos de la tabla
            Object id = table.getValueAt(selectedRow, 0);
            Object name = table.getValueAt(selectedRow, 1);
            Object desc = table.getValueAt(selectedRow, 2);
            Update sendSelectedRow = new Update(id.toString(), name.toString(), desc.toString()); // Env√≠a los datos a la interfaz "Update"
            sendSelectedRow.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null,"You must select a branch");
        }
        
    }

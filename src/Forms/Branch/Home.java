    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableBranch.getSelectedRow();
        if (selectedRow != -1) {
            Object id = tableBranch.getValueAt(selectedRow, 0);
            Object name = tableBranch.getValueAt(selectedRow, 1);
            
            int confirm = JOptionPane.showConfirmDialog(null,"Are you sure to delete it?");
            
            if(confirm == JOptionPane.YES_OPTION){
                ResultSetIES9021 resultDelete = Branch.delete((int) id, name.toString());
                if(resultDelete.getState()){
                    JOptionPane.showMessageDialog(null,resultDelete.getClarification());
                }else{
                    JOptionPane.showMessageDialog(null,resultDelete.getClarification());
                }
            }
        }else{
            JOptionPane.showMessageDialog(null,"You must select a branch");
        }
            
    }//GEN-LAST:event_btnDeleteActionPerformed

